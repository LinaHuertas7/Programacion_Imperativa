 // Dato del tipo number

let edad = 17;
console.log (edad)//17

// Dato del tipo number decimales

let precio = 150.50;
console.log (precio)//150.50

// Dato del tipo string con comillas dobles

let nombre = "Lina";
console.log (nombre)//Lina

// Dato del tipo string con comillas simples

let apellido = 'Huertas';
console.log(apellido)//Huertas

// Dato buleano true

let verdadero = true;
console.log(verdadero)//True

// Dato buelano false

let falso = false;
console.log (falso);//False

// Dato del tipo NaN

let notANumber = NaN;
console.log(notANumber);//NaN

// Dato del tipo Null
let nulo = null;
console.log(nulo);//null

// Dato undefined

let sinValor = "Sin Valor";
console.log(sinValor);//Sin Valor

// Dato objeto literal

let persona ={
    nombre: 'Lina',
    edad: 17
}
console.log (persona);//{ nombre: 'Lina', edad: 17 }

// Dato del tipo array

let paises= ['Argentina','Italia', 'Colombia']
console.log (paises);//[ 'Argentina', 'Italia', 'Colombia' ]

